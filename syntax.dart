class printOut{
  
  void outPut(){
    
    print("hello world");
  }
  
 
}

void main(){
    
    printOut c = new printOut();
    
    c.outPut();
  }

/*
The above example defines a class printOut. The class has a method outPut(). The method prints the string “Hello World” on the terminal. The new keyword creates an object of the class. The object invokes the method outPut()
